function comparingNumbers(a, b) {
    console.log(`${a} > ${b}:`, a > b);
    console.log(`${a} >= ${b}:`, a >= b);
    console.log(`${a} < ${b}:`, a < b);
    console.log(`${a} <= ${b}:`, a <= b);
    console.log(`${a} == ${b}:`, a == b);
    console.log(`${a} != ${b}:`, a != b);
}

comparingNumbers(7, 6);
